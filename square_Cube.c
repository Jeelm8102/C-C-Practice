#include <stdio.h>
//SQUARE AND CUBE
int main()
{
    int num;
    printf("Enter the number:\n");
    scanf("%d",&num);

    printf("The given number is:%d.\n",num);
    printf("The Square of given number is:%d.\n",num*num);
    printf("The cube of given number is:%d.\n",num*num*num);
    return 0;

}